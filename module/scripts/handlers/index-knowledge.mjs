/**
 * index_knowledge — Persiste conocimiento generado por el agente en el RAG.
 *
 * args: {
 *   module: string,       // ID del módulo (ej. "token-magic-fx")
 *   content: string,      // Contenido markdown del conocimiento
 *   title?: string,       // Título (default: module)
 * }
 *
 * Returns: { ok: boolean, module: string }
 */

const RAG_URL = "http://127.0.0.1:7402";

export async function indexKnowledge({ module, content, title }) {
  if (!module || !content) {
    throw new Error("Se requieren 'module' y 'content'.");
  }

  // Enviar al RAG server para indexar
  const response = await fetch(`${RAG_URL}/index-document`, {
    method: "POST",
    headers: { "content-type": "application/json" },
    body: JSON.stringify({
      text: content,
      metadata: {
        module,
        title: title ?? module,
        source: "agent-generated",
        kind: "knowledge",
        symbol: module,
        parent: "knowledge",
        description: `Knowledge generated by PI agent for module ${module}`,
        line: 0,
        foundry_version: game.version,
      },
    }),
  });

  if (!response.ok) {
    const err = await response.text();
    throw new Error(`RAG index failed (${response.status}): ${err}`);
  }

  const result = await response.json();
  console.log(`[pi-bridge] ✓ Knowledge indexed for module "${module}"`);

  return { ok: true, module, ...result };
}
